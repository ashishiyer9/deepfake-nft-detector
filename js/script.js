async function uploadToIPFS(blob) {
  const res = await ipfs.add({
    content: blob,
  }, {
    cidVersion: 1,
    hashAlg: 'sha2-256'
  });
  cid = res.cid;

  console.log("Successfully stored", cid);
  document.getElementById("result").innerText = `CID: ${cid}`;
}


